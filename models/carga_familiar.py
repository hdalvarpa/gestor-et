class CargaFamiliar:
    def __init__(self, 
                 nombres: str = "", 
                 dni: str = "", 
                 nacimiento: str = "", 
                 vinculo: str = "", 
                 instruccion: str = "", 
                 discapacidad: str = ""):
        self.nombres = nombres
        self.dni = dni
        self.nacimiento = nacimiento
        self.vinculo = vinculo
        self.instruccion = instruccion
        self.discapacidad = discapacidad
